
public class Courses {
	
	public Courses(int id, String courseName, String courseDetails, String instructor, double coursePrice) {
		
		this.id = id;
		this.courseName = courseName;
		this.courseDetails = courseDetails;
		this.instructor = instructor;
		this.coursePrice = coursePrice;
		
	}
	int id;
	String courseName;
	String courseDetails;
	String instructor;
	double coursePrice;
}
